package Exercicio04;

public class ParOuImpar {
    public static void main(String[] args) {
        int num = 25;

        if (num  % 2 == 0) {
            System.out.println("O número " + num + " é par.");
        } else {
            System.out.println("O número " + num + " é ímpar.");
        }
    }
}
